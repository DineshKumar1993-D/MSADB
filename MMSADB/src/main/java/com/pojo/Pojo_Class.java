package com.pojo;

public class Pojo_Class {
	
	// Pojo --> Plain old java object
	private String name;
	private int age;
	private char gen;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public char getGen() {
		return gen;
	}
	public void setGen(char gen) {
		this.gen = gen;
	}
	

}
